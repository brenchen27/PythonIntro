# 1.) A class is a way to bundling data and other information together. An object is a function within the class.
# 2.) A class variable is a function that works throughout all of the different classes. An instance variable is a function unique to an object.
# 3.) Inheritance is what subclasses do when they take other methods and functions from the main class.
# 4.) The child class are the classes within the parent class. The parent class is the main class. Subclass is another name for child class and super class is another name for parent class.

